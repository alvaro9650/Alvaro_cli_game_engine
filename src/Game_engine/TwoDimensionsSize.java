public class TwoDimensionsSize {

    public Integer x;
    public Integer y;

    /**
     * Creates an object to save a 2D size
     *
     * @param x X size
     * @param y Y size
     */
    public TwoDimensionsSize(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }
}
