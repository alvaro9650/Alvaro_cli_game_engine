package Game_engine;

/**
 *
 * @author alvaro9650
 */
public class ThreeDimensionsSize {

    public Integer x;
    public Integer y;
    public Integer z;

    /**
     * Creates an object to save a 3D size
     *
     * @param x X size
     * @param y Y size
     * @param z Z size
     * @author alvaro9650
     */
    public ThreeDimensionsSize(Integer x, Integer y,Integer z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
